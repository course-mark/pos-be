import { model, Schema, Document } from 'mongoose';
import { User } from '@interfaces/users.interface';

const customerSchema: Schema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  phone: {
    type: Number,
  },
  address: {
    type: String,
  },
  city: {
    type: String,
  },
  state: {
    type: String,
  },
  country: {
    type: String,
  },
});

export const customerModel = model('customer', customerSchema);
